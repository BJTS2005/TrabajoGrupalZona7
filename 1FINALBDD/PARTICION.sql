CREATE TABLE EVENTOS_SOSTENIBLES (
    ID_EVE VARCHAR(5) NOT NULL,
    TPE_ID VARCHAR(5) NOT NULL,
    CAMP_ID VARCHAR(5) NOT NULL,
    EVE_DETALLE TEXT NOT NULL,
    EVE_FECHA DATE NOT NULL,
    EVE_POR_ESTUDIANTES BOOL NOT NULL,
    EVE_URL TEXT NULL,
    PRIMARY KEY (ID_EVE, EVE_FECHA)
)
PARTITION BY RANGE (EVE_FECHA);

